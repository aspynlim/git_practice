# Basic Calculator
def add(a, b):
    return a+b

def subtract(a, b):
    return a-b

def multiply(a, b):
    return a*b

def divide_FREE(a, b):
    return a/b

def remainder(a, b):
    return a//b

def get_abs(num):
    if num >=0:
        return num
    else:
        return -num

def percentage(a, b):
    return (a/b)*100